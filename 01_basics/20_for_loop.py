# Petla for

# wypisanie kolejnych znakow w slowie
for letter in "Python":
    print(letter)

print("\n")

# wypisanie kolejnych elementow listy
friends = ["Tom", "Jimi", "Ann"]
for friend in friends:
    print(friend)

# Wypis indeksy pierwsze 10 ale zaczynamy od 0 wiec bez 10
for index in range(10):
    print(index)

# Wypis indexy od 3 do 10 ale bez 10 tu bedzie
for index in range(3, 10):
    print(index)
